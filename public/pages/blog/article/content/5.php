<section id="layout">
    <h2>Где разместить рабочую зону: 4 сценария</h2>
    <p>Не у всех есть отдельный кабинет. Вот 4 реалистичных варианта размещения:</p>
    <ul>
        <li><strong>У окна в гостиной</strong> — естественный свет, зона не мешает проходу. Минус: если телевизор работает, сложно сосредоточиться.</li>
        <li><strong>В спальне</strong> — тихо и уединённо. Главное — отделить рабочую зону от кровати, чтобы не нарушать сон.</li>
        <li><strong>На лоджии</strong> — утеплённая лоджия становится идеальным кабинетом. В 2026 это самый популярный тренд.</li>
        <li><strong>За шкафом-купе</strong> — откидной стол в нише шкафа. Решение для микроквартир до 30 м².</li>
    </ul>

