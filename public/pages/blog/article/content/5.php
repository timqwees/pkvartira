<section id="light">
    <h2>Освещение для продуктивности</h2>
    <p>Неправильный свет — главный враг продуктивности. Вот правила освещения рабочей зоны:</p>
    <ul>
        <li>Основной свет — потолочный светильник с холодной температурой 4000–5000K.</li>
        <li>Настольная лампа слева (для правшей) или справа (для левшей).</li>
        <li>Избегайте бликов на экране — не ставьте стол напротив окна.</li>
        <li>LED-лента по периметру монитора снижает нагрузку на глаза.</li>
    </ul>
