<section id="zoning">
    <h2>Зонирование без стен</h2>
    <p>Отделить рабочую зону от жилой помогут дизайнерские приёмы без капитальных стен:</p>
    <ul>
        <li><strong>Стеллаж-перегородка</strong> — функционально: с одной стороны книги, с другой — растения или декор.</li>
        <li><strong>Шторы-нити</strong> — визуально отделяют зону, не съедая пространство.</li>
        <li><strong>Разный цвет стен</strong> — акцентная стена в рабочей зоне (глубокий синий или тёмно-зелёный настраивает на работу).</li>
        <li><strong>Ковёр-граница</strong> — ковёр на полу визуально выделяет зону.</li>
    </ul>

