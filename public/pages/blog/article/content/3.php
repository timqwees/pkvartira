<section id="plan">
    <h2>Пошаговый план обновления</h2>

    <h3>Шаг 1: Оценка состояния</h3>
    <p>Проверьте стены на трещины, пол — на скрипы, потолок — на пятна. Составьте список дефектов. Если стены ровные — хватит шпаклёвки. Если нет — придётся штукатурить.</p>

    <h3>Шаг 2: Выбор материалов</h3>
    <p>Определите цветовую гамму и стиль. В 2026 в тренде тёплые нейтральные оттенки: «латте», «пыльная роза», «шампань». Избегайте кричащих цветов — они быстро надоедят.</p>

    <h3>Шаг 3: Демонтаж старого</h3>
    <p>Снимите обои, демонтируйте старый плинтус и двери. Если пол в хорошем состоянии — кварцвинил можно укладывать поверх.</p>

    <h3>Шаг 4: Подготовка поверхностей</h3>
    <p>Грунтовка, шпаклёвка, шлифовка. На этом этапе нельзя экономить — качество финиша напрямую зависит от подготовки.</p>

    <h3>Шаг 5: Финишная отделка</h3>
    <p>Потолок → стены → пол. Именно в такой последовательности, чтобы не испачкать готовые поверхности.</p>

    <div data-type="box" data-color="blue">
        <h3>Нужен дизайн-проект для косметического ремонта?</h3>
        <p>Многие считают, что дизайн-проект нужен только для капитального ремонта. На самом деле, цветовые схемы, расстановка мебели и подбор материалов помогут избежать ошибок даже при небольшом бюджете.</p>
    </div>
</section>

<section id="faq">
    <h2>FAQ — косметический ремонт</h2>
    <p><strong>Вопрос:</strong> Сколько времени занимает косметический ремонт?<br>
    <strong>Ответ:</strong> В среднем 2–4 недели. Однокомнатная квартира — 10–14 дней, «двушка» — до 3 недель.</p>
    <p><strong>Вопрос:</strong> Нужно ли выезжать из квартиры?<br>
    <strong>Ответ:</strong> Желательно, но не обязательно. Если ремонт идёт поэтапно, можно оставаться — особенно при использовании быстросохнущих материалов.</p>
    <p><strong>Вопрос:</strong> Можно ли сделать косметический ремонт своими руками?<br>
    <strong>Ответ:</strong> Покраска стен и замена линолеума доступны новичку. Штукатурку и натяжные потолки лучше доверить профессионалам.</p>
</section>