<section id="what">
    <h2>Что такое косметический ремонт</h2>
    <p>Косметический ремонт — это обновление видимых поверхностей без замены инженерных систем. Грубо говоря, всё, что находится «над стяжкой» и «перед трубами».</p>
    <ul>
        <li>Стены: покраска, поклейка обоев, декоративная штукатурка.</li>
        <li>Потолок: побелка, покраска, натяжные конструкции.</li>
        <li>Пол: замена напольного покрытия (ламинат, линолеум, кварцвинил).</li>
        <li>Двери и плинтусы: замена или обновление.</li>
        <li>Сантехника: замена смесителей, унитаза, раковины (без переноса).</li>
    </ul>

    <div data-type="box" data-color="dark">
        <h3>Граница косметического ремонта</h3>
        <p>Как только вы начинаете штробить стены, менять трубы или переносить розетки — это уже капитальный ремонт. Для косметического достаточно «мокрых» процессов: шпаклёвка, штукатурка, покраска.</p>
    </div>
</section>

