<section id="combine">
    <h2>Как сочетать тренды в одном интерьере</h2>
    <p>Главный навык современного дизайна — миксование текстур и эпох. Вот работающее правило:</p>
    <ul>
        <li><strong>База (60%)</strong> — нейтральные стены и пол (тёплая слоновая кость + дерево).</li>
        <li><strong>Акценты (30%)</strong> — цветная мебель, текстиль, декор (пыльная роза или шалфей).</li>
        <li><strong>Детали (10%)</strong> — металл, стекло, керамика (латунь, состаренная бронза, рифлёное стекло).</li>
    </ul>
    <p>Это правило 60-30-10 работает безотказно. Оно же помогает не переборщить с трендами.</p>
</section>

