    <h2>Палитра 2026: 5 главных цветов</h2>
    <p>По версии ведущих дизайн-бюро, 2026 год — время тёплой нейтральной палитры. Вот 5 цветов, которые будут в топе:</p>
    <ul>
        <li><strong>«Тёплая слоновая кость»</strong> — базовый стеновой цвет. Заменяет холодный белый. Зрительно согревает помещение.</li>
        <li><strong>«Пыльная роза»</strong> — приглушённый розово-серый. Идеален для спальни и гостиной.</li>
        <li><strong>«Шалфей»</strong> — серо-зелёный оттенок. Спокойный и благородный. Хорош для кухни и кабинета.</li>
        <li><strong>«Глиняный»</strong> — терракотовый с серым подтоном. Для акцентных стен и текстиля.</li>
        <li><strong>«Графит»</strong> — глубокий тёмно-серый. Для кухонных фасадов, рам окон, дверей.</li>
    </ul>

    <div data-type="box" data-color="gray">
