<section id="pifall">
    <h2>Подводные камни договора</h2>
    <p>Даже с проверенной компанией стоит внимательно изучить договор. Типичные ловушки:</p>
    <ul>
        <li><strong>Расплывчатые сроки</strong> — вместо «45 рабочих дней» написано «по факту готовности».</li>
        <li><strong>Открытая смета</strong> — цена может меняться без согласования.</li>
        <li><strong>Исключение материалов</strong> — в смете не указаны конкретные бренды, подрядчик ставит дешёвые аналоги.</li>
        <li><strong>Штрафные санкции</strong> — штрафы только для заказчика, но не для подрядчика за срыв сроков.</li>
    </ul>

    <div data-type="box" data-color="gray">
