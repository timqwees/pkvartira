<section id="price">
    <h2>Что входит в ремонт под ключ: состав работ</h2>
    <p>Важно понимать: «под ключ» — не магическая фраза, а конкретный перечень этапов. В типовой договор входят:</p>
    <ul>
        <li>Замер и дизайн-проект (планировка, визуализация, чертежи).</li>
        <li>Демонтаж и вывоз мусора.</li>
        <li>Инженерия: электрика, сантехника, отопление, слаботочка.</li>
        <li>Черновая отделка: стяжка, штукатурка, шпаклёвка.</li>
        <li>Чистовая отделка: плитка, покраска, напольные покрытия.</li>
        <li>Монтаж дверей, плинтусов, сантехники и освещения.</li>
        <li>Уборка и вывоз строительного мусора.</li>
    </ul>

    <div data-type="box" data-color="dark">
        <h3>Важно</h3>
        <p>Уточняйте, включена ли в договор финишная уборка и вывоз мусора. Эти пункты нередко идут как опция за дополнительную плату.</p>
    </div>
</section>

