<section id="architect">
    <h2>Архитектурный подход: что это значит</h2>
    <p>Главный тренд 2026 года — отказ от декора ради декора. Архитектурный подход подразумевает:</p>
    <ul>
        <li><strong>Работу с пространством</strong> — объём, свет, материалы, а не количество деталей.</li>
        <li><strong>Встроенные системы</strong> — скрытые двери, плинтусы невидимки, спрятанная техника.</li>
        <li><strong>Монохромные палитры</strong> — один цвет в разных фактурах создаёт глубину без пёстроты.</li>
        <li><strong>Минимализм с характером</strong> — минимум мебели, максимум качества.</li>
    </ul>

    <div data-type="box" data-color="dark">
        <h3>Умный дом как часть архитектуры</h3>
        <p>В премиум-сегменте умный дом интегрируется на этапе проекта: шторы, свет, климат, безопасность — всё управляется с одной панели и голосом.</p>
    </div>
</section>

