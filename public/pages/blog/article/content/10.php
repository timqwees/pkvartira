<section id="budget">
    <h2>Сколько стоит дизайнерский ремонт в 2026</h2>
    <p>Цены на дизайнерский ремонт в 2026 году стартуют от 35 000 ₽/м² и не имеют верхней границы:</p>
    <ul>
        <li>Дизайн-проект — 3 000–10 000 ₽/м² (в зависимости от сложности).</li>
        <li>Черновые работы — 10 000–15 000 ₽/м².</li>
        <li>Чистовые материалы — от 15 000 ₽/м².</li>
        <li>Мебель на заказ — от 1 000 000 ₽ за типовой набор.</li>
        <li>Авторский надзор — 10–15% от бюджета работ.</li>
    </ul>

