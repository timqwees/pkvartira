<section id="tech">
    <h2>Встраиваемая техника: must-have</h2>
    <p>В 2026 году отдельностоящая техника на кухне — моветон. Встройка — не только эстетика, но и эргономика:</p>
    <ul>
        <li>Холодильник скрытый за фасадом — единая линия кухни.</li>
        <li>Духовка на уровне глаз — не нужно нагибаться.</li>
        <li>Посудомоечная машина на 45 см — помещается даже в маленькую кухню.</li>
        <li>Вытяжка встроенная в столешницу (у носителей индукции) — тренд 2026.</li>
    </ul>

    <div data-type="box" data-color="dark">
        <h3>Не экономьте на технике</h3>
        <p>Посудомоечная машина окупается экономией воды + времени. Индукционная панель безопаснее и быстрее газовой. Холодильник с No Frost — никакой разморозки.</p>
