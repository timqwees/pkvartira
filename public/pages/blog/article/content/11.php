<section id="trends">
    <h2>Тренды кухонь 2026</h2>
    <p>Что в топе в этом году:</p>
    <ul>
        <li><strong>Матовые фасады</strong> — без глянца и отпечатков пальцев. Цвета: графит, шампань, оливковый.</li>
        <li><strong>Кварцевый агломерат в столешнице</strong> — прочнее натурального камня и без пор.</li>
        <li><strong>Фартук из керамогранита</strong> — бесшовный (до 3 м длиной), легко моется.</li>
        <li><strong>Скрытая фурнитура</strong> — ручки-профили или системы push-to-open.</li>
        <li><strong>Остров</strong> — даже на кухне 10 м² можно вписать компактный остров на колёсах.</li>
    </ul>

