<section id="layout">
    <h2>Планировка кухни: рабочий треугольник</h2>
    <p>Золотое правило кухонной эргономики — рабочий треугольник «плита — мойка — холодильник». Расстояние между вершинами: 1.2–2.7 метра. Сумма сторон — не более 8 метров.</p>
    <p>В 2026 году популярны три типа планировки:</p>
    <ul>
        <li><strong>Линейная</strong> — для узких кухонь. Всё вдоль одной стены. Плюс: дёшево. Минус: много ходьбы.</li>
        <li><strong>Угловая (L-образная)</strong> — классика. Вмещает уголок для завтраков. Рабочий треугольник оптимален.</li>
        <li><strong>П-образная</strong> — для просторных кухонь от 12 м². Всё под рукой, много хранения.</li>
    </ul>

    <div data-type="box" data-color="gray">
