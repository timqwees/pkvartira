<section id="integrate">
    <h2>Как объединить устройства в единую систему</h2>
    <p>Разрозненные устройства от разных брендов — это не умный дом, а зоопарк. В 2026 году есть три основных экосистемы:</p>
    <ul>
        <li><strong>Яндекс.Алиса</strong> — самая популярная в России. Совместима с Xiaomi, Philips, Tuya. Подходит для 90% задач.</li>
        <li><strong>Apple HomeKit</strong> — для фанатов Apple. Закрытая, но безопасная экосистема.</li>
        <li><strong>Home Assistant</strong> — open-source решение для гиков. Объединяет любые устройства, но требует настройки.</li>
    </ul>

    <div data-type="box" data-color="gray">
        <h3>Совет новичкам</h3>
        <p>Начинайте с одной экосистемы (рекомендуем Яндекс). Покупайте устройства с логотипом «Работает с Алисой» — они гарантированно дружат между собой.</p>
