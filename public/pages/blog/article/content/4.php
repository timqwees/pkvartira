<section id="musthave">
    <h2>Must-have: что реально нужно</h2>
    <p>Вот список устройств, которые окупают себя в первые же месяцы и реально упрощают жизнь:</p>

    <ul>
        <li><strong>Умное отопление</strong> — термостаты с погодным регулированием экономят до 30% на отоплении. В 2026 это не роскошь, а необходимость.</li>
        <li><strong>Робот-пылесос с картографией</strong> — современные модели (Xiaomi, Dreame, Roborock) не царапают мебель и моют полы.</li>
        <li><strong>Умные розетки с контролем энергии</strong> — показывают потребление каждого прибора и отключают питание удалённо.</li>
        <li><strong>Водный извещатель</strong> — датчик протечки перекрывает воду при аварии. Страховка от потопа ценой 3 000 ₽.</li>
        <li><strong>Умный дверной замок</strong> — вход по отпечатку пальца или коду. Забыли ключи? Не проблема.</li>
    </ul>

    <div data-type="box" data-color="gray">
        <h3>Сколько стоит базовый набор</h3>
        <p>Базовый комплект «умного дома» для однокомнатной квартиры: датчик протечки (3 000 ₽), 2 термостата (10 000 ₽), робот-пылесос (35 000 ₽), 3 умные розетки (6 000 ₽). Итого: ~55 000 ₽.</p>
    </div>
</section>

