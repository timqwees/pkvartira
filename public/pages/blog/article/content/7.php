<section id="master">
    <h2>Мастер-спальня: гардеробная + санузел</h2>
    <p>Концепция мастер-спальни пришла из отельного дизайна. Это спальня с собственным санузлом и гардеробной. В 2026 году это уже не люкс, а стандарт комфорта.</p>
    <p>Чтобы организовать мастер-спальню, нужно:</p>
    <ul>
        <li>Выделить часть смежной комнаты под гардеробную (3–5 м²).</li>
        <li>Расширить санузел за счёт коридора (если позволяет планировка).</li>
        <li>Установить раздвижные двери для экономии места.</li>
    </ul>

    <div data-type="box" data-color="gray">
        <h3>Совет планировщика</h3>
        <p>Минимальная площадь мастер-спальни — 16 м². Из них 8 м² — спальная зона, 4 м² — гардеробная, 4 м² — санузел с душевой.</p>
    </div>
</section>

