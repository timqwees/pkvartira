<section id="heat">
    <h2>Умное отопление и рекуперация</h2>
    <p>Самые эффективные инвестиции в энергосбережение:</p>
    <ul>
        <li><strong>Термостаты с погодным регулированием</strong> — автоматически снижают температуру, когда вас нет дома. Экономия до 30%.</li>
        <li><strong>Рекуператор воздуха</strong> — подогревает приточный воздух за счёт вытяжного. Экономия на отоплении до 40%.</li>
        <li><strong>Тёплый пол с программированием</strong> — работает только в часы присутствия.</li>
    </ul>

    <div data-type="box" data-color="dark">
        <h3>Важно</h3>
        <p>Рекуператор имеет смысл ставить только в герметичных квартирах с хорошими окнами. Иначе эффект нулевой.</p>
    </div>
