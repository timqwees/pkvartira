<section id="warm">
    <h2>Утепление стен, пола, потолка</h2>
    <p>До 40% тепла уходит через стены и пол. Первым делом — инспекция теплопотерь тепловизором.</p>
    <ul>
        <li><strong>Стены:</strong> Минеральная вата или пенополистирол. Оптимальная толщина для Подмосковья — 100–150 мм.</li>
        <li><strong>Пол:</strong> Утепление стяжки пенополистиролом экструдированным (XPS). Толщина — 30–50 мм.</li>
        <li><strong>Потолок:</strong> Если сверху холодный чердак — утепление обязательно. В многоквартирном доме менее критично.</li>
    </ul>

    <div data-type="box" data-color="gray">
        <h3>Экономия</h3>
        <p>Комплексное утепление квартиры снижает теплопотери на 40–60%. Срок окупаемости — 2–3 отопительных сезона.</p>
    </div>
</section>

