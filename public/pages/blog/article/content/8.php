<section id="wait">
    <h2>Ждать ли усадку дома</h2>
    <p>Кирпичные и монолитные дома дают минимальную усадку (до 1–2 мм на этаж). Можно начинать ремонт сразу после получения ключей. Панельные дома — усадка до 3–5 мм, рекомендуется подождать 6–12 месяцев.</p>

    <div data-type="box" data-color="dark">
        <h3>Главное правило 2026</h3>
        <p>Даже если вы не ждёте усадку, не делайте чистовую отделку первый год. Сделайте стяжку, штукатурку и инженерию — и дайте дому «отстояться» 8–12 месяцев.</p>
    </div>
</section>

<section id="stage">
    <h2>Этапы ремонта новостройки</h2>
    <p>Правильная последовательность — залог того, что не придётся переделывать:</p>
    <ul>
        <li><strong>Этап 1:</strong> Перепланировка и демонтаж (если нужны).</li>
        <li><strong>Этап 2:</strong> Инженерия — электрика, сантехника, отопление, слаботочка.</li>
        <li><strong>Этап 3:</strong> Стяжка пола и штукатурка стен.</li>
        <li><strong>Этап 4:</strong> Шпаклёвка, подготовка под чистовую.</li>
        <li><strong>Этап 5:</strong> Чистовая отделка: пол, стены, потолок.</li>
        <li><strong>Этап 6:</strong> Монтаж дверей, сантехники, освещения.</li>
        <li><strong>Этап 7:</strong> Мебель и декор.</li>
    </ul>

